for counter in range (0,TIME):
            
    for row in range (0,row_size):
        for collum in range (0,collum_size):
            
            OLD[row][collum]=NEW[row][collum]

            if OLD[row][collum] == -1:
                NEW [row][collum] == -1

            elif OLD[row][collum] == 1:
                NEW [row][collum] == 1
                
